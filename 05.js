// 암시적 형변환

let result1 = 1 + "2";
console.log(result1);
console.log(typeof result1);

// 명시적 형변환

let result2 = "1" + true;
console.log(result2);
console.log(typeof result2);

// 숫자형
let result3 = 1 - "2";
console.log(result3);
console.log(typeof result3);

let result4 = "2" * "3";
console.log(result4);
console.log(typeof result4);